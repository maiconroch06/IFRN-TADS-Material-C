// while (enquanto), laço infinito (evitar utilizar)
// do ... while (faça .. enquanto)
// for (para)

#include <stdio.h>

int main (void) {
    int cont = 0;
        //    !=
    while (cont < 10) {
        printf("\n %i. PROGRAMACAO DE COMPUTADORES!", ++cont);
    }

    printf("\n\n #Final do programa");

	return 0;
}
